public class Calculator {

    public long add(int num1, int num2){
        return num1 + num2;
    }

    public long subtract(int num1, int num2){
        return num1 - num2;
    }

    public long multiply(int num1, int num2){
        return num1 * num2;
    }

    public double divide(double num1, double num2){
        return num1 / num2;
    }
}





